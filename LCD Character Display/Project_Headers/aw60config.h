#ifndef AW60CONFIG_H_
#define AW60CONFIG_H_

void configGPIO();
void configMicro();
void startMicro();


void configMicro(){
	SOPT = 0x00;                // Watchdog timer disabled
}


void configGPIO(){

// All GPIO port pins as output to prevent energy waste in CMOS inputs
    PTADD = 0xFF;
    PTBDD = 0xFF;
	PTCDD = 0xFF;
	PTDDD = 0xFF;
	PTEDD = 0xFF;
	PTFDD = 0xFF;
	PTGDD = 0xFF;
}


void startMicro(){
	configMicro();
	configGPIO();
}

#endif /* AW60CONFIG_H_ */

