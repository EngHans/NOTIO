void delay(int i, int j);       // Customizable delay

void delay (int i, int j){
	int ii = 0;
	int jj = 0;
	for (ii = 0; ii<i; ii++)
	{
		for(jj = 0; jj<j; jj++)
		{
			_asm NOP;
		}
	}
}

